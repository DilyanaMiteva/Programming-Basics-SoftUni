public class NumbersEndingIn7_01 {
    public static void main(String[] args) {
        //Принтираме вс числа от 1 до 1000 които завършват на 7
        // За числата от 1 до 1000 проверяваме дали завършват на 7
        for(int i = 7; i <= 997; i=i+10){
            System.out.println(i);
        }
    }
}